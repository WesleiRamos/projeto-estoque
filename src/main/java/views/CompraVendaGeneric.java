    private void openCompraVendaModal(Actions action) {
        try {
            CompraVendaEditor<DAO, BEAN> window = new CompraVendaEditor<>(c_dao, c_bean, selectedId, store, action);
            window.addWindowListener(new WindowAdapter() {
                @Override
                public void windowClosed(WindowEvent event) {
                    super.windowClosed(event);
                    store.set("edited", null);
                }
            });
            window.setVisible(true);
        } catch (Exception ex) {
            Alerts.error(null, "Erro ao executar a ação", String.format("%s", ex));
        }
    }
    

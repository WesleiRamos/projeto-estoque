    public CompraVendaGeneric(Class<DAO> c_dao, Class<BEAN> c_bean, String title, int selectedId) {
        super(c_dao, c_bean);
        initComponents();
        
        this.selectedId = selectedId;
        if (title.endsWith("venda"))
            whereField = "id_venda";
        
        /**
         * Soma os produtos e mostra o total
         */
        this.store.subscribe("data", (value) -> {
            BigDecimal sum = new BigDecimal(0);
            ArrayList<BEAN> data = (ArrayList<BEAN>) value;
            try {
                for (int i = 0; i < data.size(); i++) {
                    BEAN bean = data.get(i);
                    BigDecimal valorUnitario = bean.getValorUnitario();
                    BigDecimal quantidade = new BigDecimal(bean.getQuantidade());
                    sum = sum.add(valorUnitario.multiply(quantidade));
                }
                
            } catch (Exception ex) {
                Alerts.error(null, "Erro ao executar a ação", String.format("%s", ex));
            }
           
            totalLabel.setText(String.format("Total: %s", GenericBean.formatNumber.format(sum)));
        });
        
        this.createSubscribersForTable(tableDados, (bool) -> {
            editarButton.setEnabled(bool);
            deletarButton.setEnabled(bool);
        });
        
        this.title.setText(String.format("Gerenciar %s", title.toLowerCase()));
    }
    
    /**
     * 
     * @return 
     */
    protected DAO getDaoInstance() {
        DAO dao = super.getDaoInstance();
        dao.where(whereField, selectedId);
        return dao;
    }

